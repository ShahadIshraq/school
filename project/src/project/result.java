    private void txt_showresultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_showresultActionPerformed
        // TODO add your handling code here:
         try{
             String str=(String) semister_box.getSelectedItem();
             String sql=null;
             String classtype=(String)class_box.getSelectedItem();
             if(str.equals("Semister 1"))sql = "select SID as ID,SName as Name,Totalmark1 as FullMark,Grade1 as Grade,Status1 as Status from '"+classtype+"'order by Totalmark1 desc,SID asc";
             else if(str.equals("Semister 2"))sql = "select SID as ID,SName as Name,Totalmark2 as FullMark,Grade2 as Grade,Status2 as Status from '"+classtype+"' order by Totalmark2 desc,SID asc";
             else if(str.equals("Annual"))sql = "select SID as ID,SName as Name,Totalmark3 as FullMark,Grade3 as Grade,Status3 as Status from '"+classtype+"' order by Totalmark3 desc,SID asc";
        pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        resulttable.setModel(DbUtils.resultSetToTableModel(rs));

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }finally{
            try{
                rs.close();
                pst.close();
                
            }
            catch(Exception e){

            }
        }
        
    }//GEN-LAST:event_txt_showresultActionPerformed

